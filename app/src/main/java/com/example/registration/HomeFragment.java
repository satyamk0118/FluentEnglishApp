public class HomeFragment extends Fragment {

    public static ArrayList<Course> listOfCourses=new ArrayList<>();
    private CourseAdapter mAdapter;
    @Nullable
    @Override
    public View onCreateView(@NonNull LayoutInflater inflater, @Nullable ViewGroup container, @Nullable Bundle savedInstanceState) {
        View view =  inflater.inflate(R.layout.home_fragment, container, false);
        ListView listView = (ListView) view.findViewById(R.id.course_list);

        mAdapter = new CourseAdapter(getContext(), listOfCourses);
        listView.setAdapter(mAdapter);


        listView.setOnItemClickListener(new AdapterView.OnItemClickListener() {
            @Override
            public void onItemClick(AdapterView<?> parent, View view, int position, long id) {
                Intent intent = new Intent(getContext(), ChapterActivity.class);
                intent.putExtra("course",listOfCourses.get(position).getCourseName());
                intent.putExtra("pos", position);
                startActivity(intent);
            }
        });
        return  view;
    }
}
